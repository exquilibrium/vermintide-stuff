local function maxn(table)
	size = 0
	for idx, value in ipairs(table) do
		size = size + 1
	end
	return size
end

-- Other tables
local Diff = {
	"Recruit",
	"Veteran",
	"Champion",
	"Legend",
	"Cataclysm"
}
local DiffToIdx = {
	Recruit = 1,
	Veteran = 2,
	Champion = 3,
	Legend = 4,
	Cataclysm = 5
}

-- Evaluate
for d = 1, 5, 1 do
    -- Open a file specific to the current value of d
    local file_name = "fow_spawns_diff_" .. d .. ".txt"
    local file = io.open(file_name, "w")

    -- Ensure the file was successfully opened
    if not file then
        error("Failed to open file for writing: " .. file_name)
    end

    local event_size = 0
    local DIFFICULTY = Diff[d]
    file:write(DIFFICULTY .. " = {\n")

    for wave_num = 1, 8, 1 do -- wave of terror events
        file:write(
            "\t{\n" ..
            "\t\t{\n"
        )

        event_size = maxn(terror_event_blueprints["plaza_wave_" .. wave_num])
        for event_id, event_data in ipairs(terror_event_blueprints["plaza_wave_" .. wave_num]) do -- singular terror event
            data = event_data
            if data[1] == "delay" then
                if not data.difficulty_requirement or DiffToIdx[data.difficulty_requirement] <= DiffToIdx[DIFFICULTY] then
                    file:write(
                        "\t\t\t{\n" ..
                        "\t\t\t\t\"delay\",\n" ..
                        "\t\t\t\tduration = " .. data.duration .. "\n" ..
                        "\t\t\t},\n"
                    )
                end
            elseif data[1] == "event_horde" then
                local breed_table = fow_horde_compositions[tostring(data.composition_type)][DiffToIdx[DIFFICULTY]][1].breeds
                local bt_size = maxn(breed_table)
                for idx = 1, bt_size, 2 do
                    file:write(
                        "\t\t\t{\n" ..
                        "\t\t\t\t\"spawn\",\n" ..
                        "\t\t\t\tspawner_id = \"" .. data.spawner_id .. "\",\n" ..
                        "\t\t\t\tbreed_id = \"" .. breed_table[idx] .. "\",\n" ..
                        "\t\t\t\tamount = " .. breed_table[idx + 1] .. "\n" ..
                        "\t\t\t},\n"
                    )
                end
            elseif data[1] == "spawn_at_raw" then
                if not data.difficulty_requirement or DiffToIdx[data.difficulty_requirement] <= DiffToIdx[DIFFICULTY] then
                    file:write(
                        "\t\t\t{\n" .. 
                        "\t\t\t\t\"spawn\",\n" ..
                        "\t\t\t\tspawner_id = \"" .. data.spawner_id .. "\",\n" ..
                        "\t\t\t\tbreed_id = \"" .. data.breed_name .. "\",\n" ..
                        "\t\t\t\tamount = 1\n" ..
                        "\t\t\t},\n"
                    )
                end
            elseif data[1] == "continue_when" then
                file:write(
                    "\t\t\t{\n" ..
                    "\t\t\t\t\"continue_when\",\n" ..
                    "\t\t\t\tduration = " .. data.duration .. ",\n" ..
                    "\t\t\t\tcondition = " .. data.condition .. "\n" ..
                    "\t\t\t}\n" ..
                    "\t\t}"
                )
                if event_id ~= event_size - 1 then
                    file:write(",\n\t\t{")
                end
                file:write("\n")
            end
        end
        file:write("\t}")
        if wave_num ~= 8 then
            file:write(",")
        end
        file:write("\n")
    end
    file:write("}")
    if d ~= 5 then
        file:write(",")
    end
    file:write("\n")

    -- Close the file after writing
    file:close()
end

